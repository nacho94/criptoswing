    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        palabra = jTextField2.getText();
        int numero = palabra.length()-1;
        String letra =palabra.charAt(numero) + "";


        if(letra.equals("0")){
            for(int i=0; i<palabra.length(); i++) {
                if(i==0){
                    palabraCompleta = palabra.charAt(i) + "";
                } else {
                    palabraCompleta = palabraCompleta + " " + palabra.charAt(i);
                }
            }
            this.dispose();
        }else {
            JOptionPane.showMessageDialog(jButton1, "No se puede anadir la palabra porque no termina en cero.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

